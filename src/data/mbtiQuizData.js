// 6. 核心动态生成器：根据 targetLength 对应的阶段，动态填充并微调题目组合
// ==========================================

export function generateProceduralQuestions(startIndex, targetLength, conflictedAxis1, conflictedAxis2, shadowTargets, activeQuestions = []) {
  
  // Helper: extract root template ID
  const getTemplateRootId = (id) => {
    const baseId = id.replace(/_idx_\d+$/, "");
    const match = baseId.match(/^(t_[a-z0-9_]+|tp_pol_[a-z0-9_]+)_(single|multi|judg)/);
    return match ? match[1] : baseId;
  };

  const usedRoots = new Set(activeQuestions.map(q => getTemplateRootId(q.id)));

  let minDifficulty = 1;
  let maxDifficulty = 1;
  let politicalCount = 0;
  let targetCount = targetLength - startIndex;

  if (targetLength <= 25) {
    // Stage 1: mostly cognitive, 1 political (making it ~15% overall)
    politicalCount = 1;
    minDifficulty = 1;
    maxDifficulty = 1;
  } else if (targetLength <= 50) {
    // Stage 2: 25 new questions, 3 political, 22 cognitive
    politicalCount = 3;
    minDifficulty = 1;
    maxDifficulty = 2;
  } else if (targetLength <= 100) {
    // Stage 3: 50 new questions, 6 political, 44 cognitive
    politicalCount = 6;
    minDifficulty = 2;
    maxDifficulty = 2;
  } else if (targetLength <= 150) {
    // Stage 4: 50 new questions, 10 political, 40 cognitive
    politicalCount = 10;
    minDifficulty = 2;
    maxDifficulty = 3;
  } else {
    // Stage 5: deep moral and stress questions
    politicalCount = 12;
    minDifficulty = 3;
    maxDifficulty = 3;
  }

  // Filter unused by checking if the template root has already been used
  const unusedCognitive = COMPILED_COGNITIVE_QUESTIONS.filter(q => !usedRoots.has(getTemplateRootId(q.id)));
  const unusedPolitical = COMPILED_POLITICAL_QUESTIONS.filter(q => !usedRoots.has(getTemplateRootId(q.id)));

  const selectedQs = [];
  let selectedPolCount = 0;

  const matchDifficulty = (q, minD, maxD) => {
    const diff = q.difficulty || 1;
    return diff >= minD && diff <= maxD;
  };

  // 1. Pick political questions
  let polCandidates = unusedPolitical.filter(q => matchDifficulty(q, minDifficulty, maxDifficulty));
  if (polCandidates.length < politicalCount) {
    polCandidates = unusedPolitical; // fallback
  }

  for (let q of polCandidates) {
    if (selectedPolCount >= politicalCount) break;
    const rootId = getTemplateRootId(q.id);
    if (!usedRoots.has(rootId)) {
      selectedQs.push(q);
      usedRoots.add(rootId);
      selectedPolCount++;
    }
  }

  // 2. Pick cognitive questions
  const cognitiveCount = targetCount - selectedQs.length;
  let cogCandidates = unusedCognitive.filter(q => matchDifficulty(q, minDifficulty, maxDifficulty));
  if (cogCandidates.length < cognitiveCount) {
    cogCandidates = unusedCognitive; // fallback
  }

  let selectedCogCount = 0;
  for (let q of cogCandidates) {
    if (selectedCogCount >= cognitiveCount) break;
    const rootId = getTemplateRootId(q.id);
    if (!usedRoots.has(rootId)) {
      selectedQs.push(q);
      usedRoots.add(rootId);
      selectedCogCount++;
    }
  }

  // 3. Fallback filler if still short: allow reusing scenarios but enforce unique question IDs
  if (selectedQs.length < targetCount) {
    const activeIds = new Set(activeQuestions.map(q => q.id.replace(/_idx_\d+$/, "")));
    const selectedIds = new Set(selectedQs.map(q => q.id));
    const allUnusedById = [...COMPILED_COGNITIVE_QUESTIONS, ...COMPILED_POLITICAL_QUESTIONS].filter(
      q => !activeIds.has(q.id) && !selectedIds.has(q.id)
    );
    for (let q of allUnusedById) {
      if (selectedQs.length >= targetCount) break;
      selectedQs.push(q);
      selectedIds.add(q.id);
    }
  }

  // 4. Ultimate fallback: if still short, clone questions with a unique duplicate ID suffix
  let dupCount = 1;
  while (selectedQs.length < targetCount) {
    const pool = [...COMPILED_COGNITIVE_QUESTIONS, ...COMPILED_POLITICAL_QUESTIONS];
    const shuffledPool = pool.sort(() => 0.5 - Math.random());
    for (let q of shuffledPool) {
      if (selectedQs.length >= targetCount) break;
      const dupQ = JSON.parse(JSON.stringify(q));
      dupQ.id = `${dupQ.id}_dup_${dupCount}`;
      selectedQs.push(dupQ);
    }
    dupCount++;
  }

  // Finalize selected questions (instantiate dynamic options if template exists)
  const finalizedQuestions = selectedQs.map((q, idx) => {
    const newQ = { ...q, id: `${q.id}_idx_${startIndex + idx}` };
    if (newQ.template) {
      const t = newQ.template;
      const allFuncs = ["Ni", "Te", "Fi", "Se", "Ne", "Ti", "Fe", "Si"];
      const shuffledFuncs = [...allFuncs].sort(() => 0.5 - Math.random());
      const idLabels = ["A", "B", "C", "D", "E", "F", "G", "H"];
      
      const options = [];
      shuffledFuncs.forEach((func, fIdx) => {
        options.push({
          id: idLabels[fIdx],
          text: t.options[func],
          weights: { [func]: 8 },
          label: `${func} 面极`
        });
      });
      newQ.options = options;
      delete newQ.template;
    }
    return newQ;
  });

  return finalizedQuestions;
}

export function getDynamicPhase1Questions() {
  const selected = [];
  const usedRoots = new Set();

  const addQuestion = (candidates, count) => {
    let added = 0;
    const shuffled = [...candidates].sort(() => 0.5 - Math.random());
    for (const q of shuffled) {
      if (added >= count) break;
      const baseId = q.id.replace(/_idx_\d+$/, "");
      const match = baseId.match(/^(t_[a-z0-9_]+|tp_pol_[a-z0-9_]+)/);
      const root = match ? match[1] : baseId;
      if (!usedRoots.has(root)) {
        selected.push(q);
        usedRoots.add(root);
        added++;
      }
    }
  };

  // 1. Pick 2 single choice cognitive (difficulty 1)
  const cogSingles = COMPILED_COGNITIVE_QUESTIONS.filter(q => q.type === 'single' && q.difficulty === 1);
  addQuestion(cogSingles, 2);

  // 2. Pick 1 multi choice cognitive (difficulty 1)
  const cogMultis = COMPILED_COGNITIVE_QUESTIONS.filter(q => q.type === 'multi' && q.difficulty === 1);
  addQuestion(cogMultis, 1);

  // 3. Pick 1 single choice political (difficulty 1)
  const polSingles = COMPILED_POLITICAL_QUESTIONS.filter(q => q.type === 'single' && q.difficulty === 1);
  addQuestion(polSingles, 1);

  if (selected.length < 4) {
    const allCandidates = [...cogSingles, ...cogMultis, ...polSingles];
    addQuestion(allCandidates, 4 - selected.length);
  }

  return selected.slice(0, 4).map((q, idx) => {
    const newQ = JSON.parse(JSON.stringify(q));
    newQ.id = `${newQ.id}_idx_${idx}`;
    newQ.phase = 1;

    if (newQ.template) {
      const t = newQ.template;
      const allFuncs = ["Ni", "Te", "Fi", "Se", "Ne", "Ti", "Fe", "Si"];
      const shuffledFuncs = [...allFuncs].sort(() => 0.5 - Math.random());
      const idLabels = ["A", "B", "C", "D", "E", "F", "G", "H"];
      newQ.options = shuffledFuncs.map((f, i) => ({
        id: idLabels[i],
        text: t.options[f],
        weights: { [f]: 8 },
        label: `${f} 面极`
      }));
      delete newQ.template;
    }
    return newQ;
  });
}

export function getDynamicPhase2Questions(activeQuestions, conflictedAxis1, conflictedAxis2, conflictedCriticAxis) {
  const getTemplateRootId = (id) => {
    const baseId = id.replace(/_idx_\d+$/, "");
    const match = baseId.match(/^(t_[a-z0-9_]+|tp_pol_[a-z0-9_]+)/);
    return match ? match[1] : baseId;
  };

  const usedRoots = new Set(activeQuestions.map(q => getTemplateRootId(q.id)));
  const selected = [];

  // 1. Select 2 political questions of difficulty 1
  const unusedPolitical = COMPILED_POLITICAL_QUESTIONS.filter(q => 
    q.type === 'single' && q.difficulty === 1 && !usedRoots.has(getTemplateRootId(q.id))
  );
  
  const shuffledPol = unusedPolitical.sort(() => 0.5 - Math.random());
  let polAdded = 0;
  for (const q of shuffledPol) {
    if (polAdded >= 2) break;
    selected.push(q);
    usedRoots.add(getTemplateRootId(q.id));
    polAdded++;
  }

  // 2. Select 2 cognitive questions from PHASE2_ADAPTIVE_POOL matching conflictedAxis1 and conflictedAxis2
  const axis1Questions = PHASE2_ADAPTIVE_POOL.filter(q => q.targetAxis === conflictedAxis1);
  const axis2Questions = PHASE2_ADAPTIVE_POOL.filter(q => q.targetAxis === conflictedAxis2);

  const shuffledAxis1 = axis1Questions.sort(() => 0.5 - Math.random());
  const shuffledAxis2 = axis2Questions.sort(() => 0.5 - Math.random());

  if (shuffledAxis1.length > 0) {
    selected.push(shuffledAxis1[0]);
    usedRoots.add(shuffledAxis1[0].id);
  }
  if (shuffledAxis2.length > 0) {
    selected.push(shuffledAxis2[0]);
    usedRoots.add(shuffledAxis2[0].id);
  } else if (shuffledAxis1.length > 1) {
    selected.push(shuffledAxis1[1]);
    usedRoots.add(shuffledAxis1[1].id);
  }

  // 3. Select 1 cognitive question from PHASE2_ADAPTIVE_POOL matching conflictedCriticAxis (Critic Axis)
  if (conflictedCriticAxis) {
    const criticQuestions = PHASE2_ADAPTIVE_POOL.filter(q => q.targetAxis === conflictedCriticAxis);
    const shuffledCritic = criticQuestions.sort(() => 0.5 - Math.random());
    if (shuffledCritic.length > 0) {
      selected.push(shuffledCritic[0]);
      usedRoots.add(shuffledCritic[0].id);
    }
  }

  while (selected.length < 5) {
    const remaining = PHASE2_ADAPTIVE_POOL.find(q => !selected.find(p => p.id === q.id));
    if (remaining) {
      selected.push(remaining);
      usedRoots.add(remaining.id);
    } else {
      break;
    }
  }

  return selected.slice(0, 5).map((q, idx) => {
    const newQ = JSON.parse(JSON.stringify(q));
    newQ.id = `${newQ.id}_idx_${idx + 4}`;
    newQ.phase = 2;
    return newQ;
  });
}

export function getDynamicPhase3Questions(activeQuestions, shadowTargets) {
  const getTemplateRootId = (id) => {
    const baseId = id.replace(/_idx_\d+$/, "");
    const match = baseId.match(/^(t_[a-z0-9_]+|tp_pol_[a-z0-9_]+)/);
    return match ? match[1] : baseId;
  };

  const usedRoots = new Set(activeQuestions.map(q => getTemplateRootId(q.id)));
  
  let p3Selected = PHASE3_SHADOW_POOL.filter(q => 
    shadowTargets.includes(q.targetShadow) && !usedRoots.has(getTemplateRootId(q.id))
  );

  const shuffledRemaining = PHASE3_SHADOW_POOL.filter(q => 
    !p3Selected.find(p => p.id === q.id) && !usedRoots.has(getTemplateRootId(q.id))
  ).sort(() => 0.5 - Math.random());

  for (const q of shuffledRemaining) {
    if (p3Selected.length >= 4) break;
    p3Selected.push(q);
  }

  while (p3Selected.length < 4) {
    const remaining = PHASE3_SHADOW_POOL.find(q => !p3Selected.find(p => p.id === q.id));
    if (remaining) {
      p3Selected.push(remaining);
    } else {
      break;
    }
  }

  return p3Selected.slice(0, 4).map((q, idx) => {
    const newQ = JSON.parse(JSON.stringify(q));
    newQ.id = `${newQ.id}_idx_${idx + 9}`;
    newQ.phase = 3;
    return newQ;
  });
}

