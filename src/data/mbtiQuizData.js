export function parseOpenEndedAnswer(text) {
  const result = {
    weights: { Ni: 0, Ne: 0, Si: 0, Se: 0, Ti: 0, Te: 0, Fi: 0, Fe: 0 },
    political: { econ: 0, auth: 0 },
    matchedKeywords: []
  };

  if (!text || text.trim().length < 15) return result;

  const cognitiveMap = {
    Si: ["过去", "回忆", "历史", "细节", "经历", "记录", "习惯", "稳定", "经验", "传统", "规矩", "安全感", "守住"],
    Ni: ["未来", "本质", "趋势", "直觉", "终极", "宿命", "象征", "信念", "宇宙", "概念", "哲学", "图景", "深远"],
    Ti: ["逻辑", "拆解", "原理", "机制", "结构", "自洽", "规则", "漏洞", "算法", "分析", "辩证", "公式", "解构"],
    Te: ["效率", "效能", "利弊", "数据", "执行", "结果", "方案", "产出", "绩效", "控制", "成本", "资源", "量化"],
    Fi: ["良知", "道德", "纯粹", "内心", "灵魂", "信仰", "自我", "尊严", "真实", "情感", "边界", "愧疚", "本真"],
    Fe: ["大家", "群体", "和谐", "包容", "氛围", "情绪", "照顾", "奉献", "人际", "社交", "共鸣", "付出", "温情"],
    Ne: ["可能性", "脑洞", "创意", "灵感", "探索", "变数", "多维", "跨界", "发散", "随机", "奇思妙想", "平行"],
    Se: ["感官", "物理", "当下", "现场", "实感", "触摸", "美感", "体验", "刺激", "狂欢", "生理", "感受", "肉体"]
  };

  const politicalMap = {
    econLeft: ["福利", "平等", "二次分配", "兜底", "资本家", "剥削", "底层", "宏观调控", "公有", "均富", "大同"],
    econRight: ["竞争", "市场", "效率", "减税", "自由贸易", "企业家", "努力", "能力", "筛选", "自食其力", "私有"],
    auth: ["秩序", "稳定", "国家", "大局", "安全", "统一", "集中", "审查", "管控", "主权", "集体", "服从"],
    lib: ["自由", "监督", "权利", "独立", "隐私", "言论", "老大哥", "批判", "怀疑", "自决", "人权", "自组织"]
  };

  // 1. 扫描八维心理词频
  Object.keys(cognitiveMap).forEach(func => {
    cognitiveMap[func].forEach(kw => {
      if (text.includes(kw)) {
        result.weights[func] += 3.5;
        result.matchedKeywords.push({ type: "cognitive", text: kw, label: `${func}: ${kw}` });
      }
    });
  });

  // 2. 扫描政治光谱词频
  politicalMap.econLeft.forEach(kw => {
    if (text.includes(kw)) {
      result.political.econ -= 4.0;
      result.matchedKeywords.push({ type: "political", text: kw, label: `经济左翼: ${kw}` });
    }
  });
  politicalMap.econRight.forEach(kw => {
    if (text.includes(kw)) {
      result.political.econ += 4.0;
      result.matchedKeywords.push({ type: "political", text: kw, label: `经济右翼: ${kw}` });
    }
  });
  politicalMap.auth.forEach(kw => {
    if (text.includes(kw)) {
      result.political.auth -= 5.5;
      result.matchedKeywords.push({ type: "political", text: kw, label: `威权秩序: ${kw}` });
    }
  });
  politicalMap.lib.forEach(kw => {
    if (text.includes(kw)) {
      result.political.auth += 5.5;
      result.matchedKeywords.push({ type: "political", text: kw, label: `个人自由: ${kw}` });
    }
  });

  return result;
}
